
public class Hoho {
	private int saucisse;
	private double merguez;

	public Hoho(int saucisse) {
		super();
		this.saucisse = saucisse;
	}

	public int getSaucisse() {
		return saucisse;
	}

	public void setSaucisse(int saucisse) {
		this.saucisse = saucisse;
	}

	/**
	 * @return the merguez
	 */
	public double getMerguez() {
		return merguez;
	}

	/**
	 * @param merguez the merguez to set
	 */
	public void setMerguez(double merguez) {
		this.merguez = merguez;
	}
}
